public void readJtable(){
        DefaultTableModel modelo = (DefaultTableModel) JTbl_Consulta.getModel();
        modelo.setNumRows(0);
        JTbl_Consulta.setRowSorter(new TableRowSorter(modelo));

        for (Computador p : dao.read()){
            modelo.addRow(new Object[]{
                p.getIdComputador(),
                p.getTipo(),
                p.getEmpresa(),
                p.getSala(),
                p.getPlacaMae(),
                p.getHD(),
                p.getPlacaRede(),
                p.getRAM(),
                p.getFonte(),
                p.getPlacaVideo(),
                p.getProcessador(),
                p.getDrive()
            });
        }
    }

